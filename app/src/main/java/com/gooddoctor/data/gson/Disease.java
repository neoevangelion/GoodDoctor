public class Disease {
    private int num;
    private String id;
    private String name;
    private String pinyinill;

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPinyinill() {
        return pinyinill;
    }

    public void setPinyinill(String pinyinill) {
        this.pinyinill = pinyinill;
    }
}
